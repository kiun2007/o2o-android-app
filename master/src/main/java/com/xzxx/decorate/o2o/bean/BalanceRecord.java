public class BalanceRecord {

    private String bandcardurl;
    private String title;
    private String date;
    private String value;

    public String getBandcardurl() {
        return bandcardurl;
    }

    public void setBandcardurl(String bandcardurl) {
        this.bandcardurl = bandcardurl;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }
}
